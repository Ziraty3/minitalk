#include "ft_printf.h"
#include <stdlib.h>

int	ft_num_len(unsigned	int num)
{
	int	len;

	len = 0;
	while (num != 0)
	{
		len++;
		num = num / 10;
	}
	return (len);
}

char	*ft_uitoa(unsigned int n)
{
	char	*num;
	int		len;

	len = ft_num_len(n);
	num = (char *)malloc(sizeof(char) * (len +1));
	if (!num)
		return (0);
	num[len] = '\0';
	while (n != 0)
	{
		num[len - 1] = n % 10 + '0';
		n = n / 10;
		len--;
	}
	return (num);
}

void	ft_print_unsigned(unsigned int n, int *printed_len)
{
	char	*num;

	if (n == 0)
		*printed_len += write(1, "0", 1);
	else
	{
		num = ft_uitoa(n);
		ft_putstr(num, printed_len);
		free(num);
	}
}
